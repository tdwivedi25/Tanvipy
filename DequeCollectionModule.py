import collections
x = collections.deque(["North America","South America",'Australia'])#a deque is an optimised list to perform insertion and deletion easily.
x.appendleft('Europe')#Adds to the left side of the deque.
print(x)
x.append('Antartica')#Adds to the right side of the deque.
print(x)
x.popleft()#Removes an element from the left side of the deque
print(x)
x.pop()#removes an element form the right side of the deque
print(x)
x.copy()#Create a shallow copy of the deque.
print(x)
x.extend('Asia')
print(x)#Extend the right side of the deque by appending elements
x.extendleft('Europe')#Extend the left side of the deque by appending elements.Reverses that element.
print(x)
print(len(x))
y=x.index('a',2,13)#Return the position of x in the deque (at or after index start and before index stop). Returns the first match or raises ValueError if not found.
print(y)
x.insert(6,'Africa')
print(x)
x.rotate(-5)#Rotate the deque n steps to the right. If n is negative, rotate to the left.
print(x)
